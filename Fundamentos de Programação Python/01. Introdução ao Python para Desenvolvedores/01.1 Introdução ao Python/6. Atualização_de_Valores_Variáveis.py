# Atualização de Valores de Variáveis

# Enunciado:
# As variáveis podem ser atualizadas para possuir novos valores a qualquer momento,
# garantindo que você possa reutilizá-las de forma eficaz.
# Os valores booleanos só podem ser True ou False, semelhante a Sim/Não.

# ## Cenário:
# O cliente ganhou na loteria! Atualize as variáveis `account_balance` e `is_millionaire`
# para refletir seu novo status.

# ## Instruções:
# 1. Atualize `account_balance` para ser 1300000.00.
# 2. Atualize a variável `is_millionaire` para que seja True.
# 3. Imprima a variável `is_millionaire` para ver seu valor booleano atualizado.

# --- Código abaixo ---

# Saldo inicial da conta do cliente
account_balance = 20000.00

# Status inicial do cliente (não é milionário)
is_millionaire = False

# Atualização do saldo da conta após ganhar na loteria
account_balance = 1300000.00

# Atualização do status para refletir que o cliente agora é milionário
is_millionaire = True

# Exibição do saldo atualizado no console
print(account_balance)

# Exibição do status atualizado no console
print(is_millionaire)