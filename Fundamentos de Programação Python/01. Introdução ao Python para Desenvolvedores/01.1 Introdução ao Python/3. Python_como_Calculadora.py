# Python como Calculadora

# Enunciado:
# Trabalhar com números e fazer cálculos são tarefas de programação muito comuns.
# Neste exercício, você precisará fazer dois cálculos simples utilizando o Python como uma calculadora.

# ## Instruções:
# 1. Imprima a soma de 19 + 17.
# 2. Imprima o resultado da subtração de 99 menos 12.

# --- Código abaixo ---

# Soma de 19 e 17
# A função print() exibe o resultado da soma no console.
print(19 + 17)

# Subtração de 12 de 99
# A função print() exibe o resultado da subtração no console.
print(99 - 12)